package comp3111.coursescraper;

public class SFQ {
	private int sfq;
	private String name;
	
	public SFQ()	{
		sfq = 0;
		name = null;
	}
	
	public void setsfq(int i)	{
		this.sfq = i;
	}
	
	public int getsfq()	{
		return sfq;
	}
	
	public void setname(String s) {
		this.name = s;
	}
	
	public String getname()	{
		return name;
	}
}
